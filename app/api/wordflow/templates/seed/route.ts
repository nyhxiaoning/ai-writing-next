// POST /api/wordflow/templates/seed - Seed built-in templates
export async function POST() {
  try {
    // Check if templates already exist
    const existing = await prisma.template.findFirst({ where: { isOfficial: true } });
    if (existing) {
      return NextResponse.json({ message: '官方模板已存在，无需重复导入', count: 0 });
    }

    let created = 0;
    for (const tmpl of SEED_TEMPLATES) {
      await prisma.template.create({ data: tmpl });
      created++;
    }

    return NextResponse.json({ message: `成功导入 ${created} 个模板`, count: created });
  } catch (error) {
    console.error('导入模板错误:', error);
    return NextResponse.json({ error: '导入失败' }, { status: 500 });
  }
}
